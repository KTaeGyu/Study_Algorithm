# ----------------------------------------------------------------------------------------------------------문제
# 자연수 N과 정수K가 주어졌을 때 이항 계수 (N/K)를 구하는 프로그램을 작성하시오.

# ------------------------------------------------------------------------------------------------------예제 입력
# 첫쨰 줄에 N과 K가 주어진다.
# 5 2

# ------------------------------------------------------------------------------------------------------예제 출력
# (N/K)를 출력한다.

N, K = int(input().split())

def factorial(num):
    if num == 1:
        return 1
    else:
        return num * factorial(num - 1)

print(factorial(N), factorial(K))

